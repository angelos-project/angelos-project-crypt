package org.angproj.crypt

import kotlin.math.min
import kotlin.system.getTimeNanos

internal actual fun entropy(size: Int): ByteArray {
    return ByteArray(min(Short.MAX_VALUE.toInt(), size)) { getTimeNanos().toByte() }
}
